import json
import copy
from typing import List


class InputInstance:
    """
    A single training/test example for simple sequence classification.

    Args:
        guid: Unique id for the example.
        text_a: string. The untokenized text of the first sequence. For single
        sequence tasks, only this sequence must be specified.
        text_b: (Optional) string. The untokenized text of the second sequence.
        Only must be specified for sequence pair tasks.
        label: (Optional) string. The label of the example. This should be
        specified for train and dev examples, but not for test examples.
    """
    def __init__(self, guid, text_a, text_b=None, label=None):
        self.guid = guid
        self.text_a = text_a
        self.text_b = text_b
        self.label = label

    def __repr__(self):
        return str(self.to_json_string())

    def set_label(self, label: str):
        self.label = label

    def set_guid(self, guid: str):
        self.guid = guid

    def to_dict(self):
        """Serializes this instance to a Python dictionary."""
        output = copy.deepcopy(self.__dict__)
        return output

    def to_json_string(self):
        """Serializes this instance to a JSON string."""
        return json.dumps(self.to_dict(), indent=2, sort_keys=True) + "\n"

    def perturbable_sentence(self):
        if self.text_b is None:
            return self.text_a
        else:
            return self.text_b

    def is_nli(self):
        if self.text_b is None:
            return False
        else:
            return True

    def length(self):
        if self.text_b is None:
            return len(self.text_a.split())
        else:
            return len(self.text_b.split())

    @classmethod
    def from_instance_and_perturb_sentence(cls, instance: "InputInstance", perturb_sent: str):
        guid = instance.guid
        label = instance.label
        if instance.text_b is None:
            text_a = perturb_sent
            text_b = None
        else:
            text_a = instance.text_a
            text_b = perturb_sent
        return cls(guid, text_a, text_b, label)
        
        
class InputFeature:
    """
    A single set of features of data.

    Args:
        input_ids: Indices of input sequence tokens in the vocabulary.
        attention_mask: Mask to avoid performing attention on padding token indices.
            Mask values selected in ``[0, 1]``:
            Usually  ``1`` for tokens that are NOT MASKED, ``0`` for MASKED (padded) tokens.
        token_type_ids: Segment token indices to indicate first and second portions of the inputs.
        label: Label corresponding to the input
    """

    def __init__(self, input_ids, attention_mask, token_type_ids, input_len, label = None):
        self.input_ids = input_ids
        self.attention_mask = attention_mask
        self.token_type_ids = token_type_ids
        self.input_len = input_len
        self.label = label

    def __repr__(self):
        return str(self.to_json_string())

    def to_dict(self):
        """Serializes this instance to a Python dictionary."""
        output = copy.deepcopy(self.__dict__)
        return output

    def to_json_string(self):
        """Serializes this instance to a JSON string."""
        return json.dumps(self.to_dict(), indent=2, sort_keys=True) + "\n"